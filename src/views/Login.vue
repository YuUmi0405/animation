<script>
import '@/assets/css/register.css'
import BackBanner from "../components/BackBanner";
import {setToken} from "../utils/auth";
import {get_user_info} from "../assets/js/api";
import {valid_status} from "../assets/js/utils";
import {DURATION} from "../assets/js/settings";

export default {
  name: "Login",
  components: {
    BackBanner,
  },
  data() {
    return {
      username: '',
      password: ''
    }
  },
  methods: {
    login() {
      // 登录
      // 校验用户名和密码是否正确
      if (!this.username || !this.password) {
        this.$message({
          message: "请输入用户名或密码",
          type: "warning",
          duration: DURATION,
        });
      } else {
        // 登录
        let data = {
          username: this.username,
          password: this.password
        }
        let res_data = this.$api.login(data)
        res_data.then(res => {
          let result = valid_status(res, {
            "success": "登录成功",
            "fail": "登录失败，请稍后再试"
          })
          if (result) {
            setToken(res.data.data.jwt)

            // 获取用户信息
            let result = get_user_info()
            result.then(res => {
              this.$store.dispatch('saveUserInfo', res.data.data);//请求回来后，把用户信息存储到VUEX里
            })

            this.$router.push('/')
          }
        })
      }
    }
  }
}

